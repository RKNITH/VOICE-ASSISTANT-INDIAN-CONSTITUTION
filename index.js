// Function to speak in Hindi
const speakFun = (input) => {
    let speakInput = new SpeechSynthesisUtterance(input);
    speakInput.rate = 1;
    speakInput.pitch = 1;
    speakInput.volume = 1;
    speakInput.lang = 'hi-IN';  // Hindi Language
    window.speechSynthesis.speak(speakInput);
};




// Function to handle voice commands related to the Indian Constitution
const handleCommands = (command) => {
    // Check if constitutionData is defined
    if (!constitutionData || typeof constitutionData !== 'object') {
        speakFun('संविधान डेटा उपलब्ध नहीं है। कृपया बाद में कोशिश करें।');
        return;
    }

    if (command.includes("संविधान")) {
        // Speak out the introduction and main features of the Constitution
        const introAndFeatures = `${constitutionData.history} ${constitutionData.mainFeatures}`;
        speakFun(introAndFeatures);
    } else {
        // Check if a specific part of the Constitution is being asked
        const partName = Object.keys(constitutionData.partsAndArticles || {}).find(part => command.includes(part));

        if (partName) {
            const partInfo = constitutionData.partsAndArticles[partName];
            const partDetails = `${partName} - ${partInfo.title}: `;
            const articles = Object.keys(partInfo.articles || {}).map(article => `${article} - ${partInfo.articles[article]}`).join(" | ");
            const response = `${partDetails} इसमें शामिल अनुच्छेद: ${articles}`;
            speakFun(response);
        }
        // Check if a specific schedule is being asked
        else {
            const scheduleName = Object.keys(constitutionData.schedules || {}).find(schedule => command.includes(schedule));

            if (scheduleName) {
                const scheduleInfo = constitutionData.schedules[scheduleName];
                const response = `${scheduleName} - ${scheduleInfo}`;
                speakFun(response);
            } else {
                speakFun('मुझे खेद है, मैं इस भाग या अनुच्छेद के बारे में जानकारी नहीं दे सकता। कृपया सही भाग या अनुच्छेद पूछें।');
            }
        }
    }
};




// Initialize speech recognition
const startRecognition = () => {
    const recognition = new (window.SpeechRecognition || window.webkitSpeechRecognition)();
    recognition.lang = 'hi-IN';  // Hindi Language for Recognition

    recognition.onstart = () => {
        console.log('Voice recognition started. Speak now!');
    };

    recognition.onspeechend = () => {
        console.log('Voice recognition ended.');
        recognition.stop();
    };

    recognition.onresult = (event) => {
        const transcript = event.results[0][0].transcript;
        console.log(`You said: ${transcript}`);
        handleCommands(transcript);
        btn.classList.remove('btn-box')
        btn.innerHTML = `<i class="fa-solid fa-microphone-lines-slash"></i> `
    };

    recognition.start();
};

// Button click event listener
btn.addEventListener('click', () => {
    startRecognition();
    btn.classList.add('btn-box')
    btn.innerHTML = `<i class="fa-solid fa-microphone-lines"></i> `
});
